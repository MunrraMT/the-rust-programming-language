fn main() {
    maior_numero_1_lista();
    println!("--------");
    maior_numero_2_listas();
    println!("--------");
    maior_numero_com_abstracao();
}

fn maior_numero_1_lista() {
    let number_list = vec![34, 50, 25, 100, 65];

    let mut largest = &number_list[0];

    for number in &number_list {
        if number > largest {
            largest = number;
        }
    }

    println!("The largest number is {largest}");
}

